                  <h3 class="text-uppercase">АВАЛОНЦЫ И ЭЛИТНЫЕ ПОДЗЕМЕЛЬЯ</h3>
                  <p><b>Авалонцы</b> - это древняя фракция, которая жила во времена великой войны между 
                    Мерлином и Морганой. Изолированные на протяжении тысячелетий, они вернулись, 
                    чтобы забрать обратно свои земли. Эти элитные враги на данный момент являются 
                    самыми мощными противниками в мире Альбиона, и бросить им вызов можно в <b>элитных 
                    случайных подземельях</b>, появляющихся во всех Т6-Т8 зонах Запределья и представляющих 
                    из себя высокоуровневое испытание для группы из 15-20 игроков.</p>
                  <p>В дополнение к ценнейшим в игре наградам, в конце элитных подземелий находятся 
                    <b>святилища</b>, предлагающие самым упорным и умелым игрокам <b>недельный бонус к славе, 
                    получаемой при убийстве монстров</b>.</p>
