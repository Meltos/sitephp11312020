                <div class="content__text">
                  <p>
                  12 августа 2020 года обновление Рассвет Авалона переведет мир Альбиона в совершенно новую эру. 
                  В нем представлены Пути Авалона - огромная, постоянно меняющаяся сеть древних дорог. На Пути можно войти через врата, 
                  появляющиеся в открытом мире, и они предназначены для того, чтобы преодолевать огромные расстояния ради путешествий, 
                  перевозки предметов и планирования атак (или ухода от нападения). За Путями находятся Дебри Авалона, представляющие из 
                  себя абсолютно новый мир для исследования, где можно найти сокровища, скопления ресурсов, а также новых существ и авалонцев 
                  для их дальнейшего уничтожения. Кроме того, небольшие гильдии, ищущие дом за пределами политических и территориальных войн 
                  Запределья, могут строить убежища и жить на Путях Авалона, что дает не только уникальные возможности, но и соответствующие испытания.
                  </p>
                  <p>
                  Проклятые подземелья - это новая механика, позволяющая одиночным игрокам испытать себя в соло-PvE в огненном мире, полном демонов, 
                  ловушек и проклятых существ. Игроки могут уничтожать орды врагов ради сокровищ и очков Бесчестья, или же врываться в подземелья других 
                  игроков и драться 1х1, чтобы забрать себе очки Бесчестья, добычу и подняться в таблице лидеров.
                  </p>
                  <p>
                  Новая линия авалонского оружия состоит из 15 предметов основного оружия и трех предметов оружия для левой руки, каждое из которых 
                  обладает собственными уникальными умениями, тем самым расширяя пласт боевой системы Albion Online. Это оружие можно крафтить из новых 
                  артефактов, которые, в свою очередь, создаются из новых фрагментов под названием авалонские осколки, падающие с авалонских монстров.
                  </p>
                  <p>
                  Дополнительные особенности обновления Рассвет Авалона включают глобальную переработку PvE-прокачки доски достижений, многочисленные 
                  улучшения удобства интерфейса, новый набор Бродячего авантюриста и многое, многое другое.
                  </p>
