                  <h3 class="text-uppercase">УБЕЖИЩА</h3>
                  <p>Убежища позволят гильдиям строить свои собственные подземные базы в черных зонах 
                    открытого мира. На территории этих мощных, автономных домов находятся <b>банк гильдии</b> 
                    и <b>свободные участки под строительство</b>, и в конечном счете базу можно будет улучшить 
                    до такой степени, что у вас появится возможность разместить у себя <b>рынок, мастерскую 
                    объединения артефактов</b> и даже <b>ратушу</b>.</p>
                  <p>Поскольку убежища требуют постоянную поддержку питанием и защиту как во время 
                    строительства, так и после него, выбор места размещения убежища также не обойдется 
                    без стратегического планирования <b>транспорта и логистики</b>, что открывает новые 
                    захватывающие возможности перед землевладельцами Альбиона.</p>
