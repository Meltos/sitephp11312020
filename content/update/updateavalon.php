                  <h3 class="text-uppercase">АВАЛОНСКАЯ БРОНЯ</h3>
                  <p>В дополнение к авалонскому скакуну, инструментам и еде, представленным ранее в 
                    сюжетном сезоне, теперь игроки смогут получать <b>артефакты и осколки</b> при убийстве 
                    монстров с Авалона. Из этой добычи можно будет скрафтить три новых комплекта 
                    <b>авалонской артефактной брони</b>. Данная броня варьируется от Т4 до Т8 экипировки и 
                    представлена в латах, коже и ткани; мощность соответствует четвертому уровню 
                    артефактов. Каждая часть брони обладает невероятным новым заклинанием - от щита, 
                    отражающего входящий урон в виде смертельно опасных снарядов, до умения, позволяющего 
                    парить над землей, приобретя иммунитет к обездвиживанию.</p>
