                  <h3 class="text-uppercase">КРИСТАЛЬНАЯ ЛИГА</h3>
                  <p>Вход на сражения в Кристальном королевстве теперь осуществляется через 
                    <b>Кристальную лигу</b>, в которой можно получить награду в виде огромного количества 
                    <b>серебра, славы и очков сезона</b>, и чем выше уровень битвы, тем богаче награда. 
                    Кристальная лига позволяет игрокам сражаться в окружении своих славных согильдийцев 
                    или в самостоятельно собранной команде, предоставляя высокоуровневым PvP-игрокам 
                    небывалую свободу выбора своего пути. Кроме того, когда игроки регистрируются в 
                    Кристальную лигу с гильдейской территории, победа <b>повышает уровень башни</b>, благодаря 
                    чему гильдия получает бонусы к сбору ресурсов, увеличенную защиту и очки сезона.</p>
