                  <h3 class="text-uppercase">Запределье</h3>
                  <p>В обновлении Квин мы увидим полностью переработанный континент с черными зонами - <b>Запределье</b>. 
                    У нового континента имеются низкоуровневые зоны по внешнему краю, и постепенно, ближе к центру, 
                    тиры локаций и ценность ресурсов увеличиваются, позволяя гильдиям с разными возможностями соревноваться 
                    на своем уровне. Территории внутри региона теперь будут иметь одинаковый префикс, а зоны порталов сменят 
                    свои названия на более четкие и понятные, что упростит их поиск и навигацию. В сочетании с другими игровыми 
                    системами - <b>убежищами, сражениями за территории в открытом мире и тремя новыми городами</b>, теперь гильдии смогут 
                    выбрать особый путь, позволяющий им полностью поселиться в Запределье - таким образом мы проходим следующую 
                    важную веху на пути Albion Online к полноценной ММО-песочнице.</p>
