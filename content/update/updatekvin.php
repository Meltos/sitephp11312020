                    боевой готовности. В дополнение к большому ряду уже существующих в игре костюмов, 
                    конвертированных в наряды, игрокам станут доступны различные <b>новые комплекты 
                    нарядов</b>. Как и в случае с оболочками скакунов, наряды можно будет приобретать 
                    на рынке или разблокировать в разделе внешнего вида в меню действий игрока - все 
