                  <h3 class="text-uppercase">СРАЖЕНИЯ ЗА ТЕРРИТОРИИ В ОТКРЫТОМ МИРЕ</h3>
                  <p>Теперь территории будут захватываться через <b>сражения в открытом мире</b>, предоставляя 
                    игрокам новые возможности для крупномасштабных битв. В дополнение к тому, что все 
                    участники гильдии смогут проявить себя и внести значительный вклад в захват земель, 
                    возможно столкновение нескольких гильдий в битве за определенную территорию, что 
                    приводит к увеличению сложности и требует более продуманные стратегии. Эта новая 
                    система дополняется представленном в сюжетном сезоне <b>дебафом зерга</b>, гарантирующим, 
                    что победа не будет достигнута одними лишь числами, а также новой механикой <b>умной 
                    очереди на вход в локацию</b>, которая действует как динамическая система подбора для 
                    переполненных локаций, поддерживая количество игроков и силы противоборствующих 
                    сторон в балансе.</p>
