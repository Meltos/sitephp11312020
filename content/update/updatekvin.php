                  <p>
                    20 января будет установлено самое большое контент-обновление с момента релиза - <b>Квин</b>.
                  </p>
                  <p>
                    Это обновление принесет <b>полную переработку континента с черными землями - Запределье</b>, 
                    благодаря которому игроки всех типов и уровней смогут конкурировать друг с другом и улучшать свое благосостояние в высокоуровневых зонах. 
                    <b>Убежища</b> позволят гильдиям жить и процветать в Запределье, выбрав себе в открытом мире место для жилья, а 
                    <b>новая система битв за территории открытого мира</b> в сочетании с <b>Кристальной лигой</b> предоставят возможность любым участникам 
                    гильдий вносить свой вклад во владение территорией и сезонные рейтинги.
                  </p>
                  <p>
                    Обновление Квин также познакомит нас с полностью новой фракцией монстров - древними и могучими <b>авалонцами</b>, 
                    вышедшими из забвения, чтобы вернуть себе земли Альбиона. Игроки могут столкнуться с авалонцами в новых <b>элитных случайных подземельях</b>, 
                    появляющихся по всему Запределью и предлагающих самый высокоуровневый на сегодняшний день PvE-контент в игре. В дополнение к мощным <b>недельным бонусам к PvE-славе</b>, 
                    в данных подземельях существует шанс получения редких авалонских осколков, которые можно будет перекрафтить в самую мощную в игре <b>артефактную броню</b>.
                  </p>
                  <p>
                    Помимо вышеперечисленных особенностей, обновление Квин порадует нас <b>гардеробом костюмов, пополнением зачарованных ресурсов - камнем</b>, 
                    огромным количеством <b>улучшений качества жизни</b>, включая новые курсоры, сочетание клавиш для просмотра экипировки игроков, улучшенные и обновленные карты, 
                    и многое, многое другое. Как для ветеранов, так и для начинающих игроков это обновление выводит Albion Online на новый уровень игр-песочниц 
                    с открытым миром и раскрывает перед ними бесчисленное количество возможностей для того, чтобы они смогли забрать свой собственный кусочек Альбиона.
                  </p>
